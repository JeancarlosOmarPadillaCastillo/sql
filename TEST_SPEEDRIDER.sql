CREATE TABLE MOTOS (
    IDMOT integer  NOT NULL,
    CATMOT varchar2(50)  NOT NULL,
    MARMOT varchar2(50)  NOT NULL,
    MODMOT varchar2(50)  NOT NULL,
    COLMOT varchar2(50)  NOT NULL,
    PRECOMMOT number(18,2)  NOT NULL,
    PREVENMOT number(18,2)  NOT NULL,
    GARMOT varchar2(20)  NOT NULL,
    STOCK integer  NOT NULL,
    ESTMOT char(1)  NOT NULL,
    CONSTRAINT MOTOS_pk PRIMARY KEY (IDMOT)
) ;

CREATE TABLE REPUESTOS (
    IDREP integer  NOT NULL,
    NOMREP varchar2(20)  NOT NULL,
    MARREP varchar2(50)  NOT NULL,
    MODREP varchar2(50)  NOT NULL,
    PRECOMREP number(18,2)  NOT NULL,
    PREVENREP number(18,2)  NOT NULL,
    STOCK integer  NOT NULL,
    ESTREP char(1)  NOT NULL,
    CONSTRAINT REPUESTOS_pk PRIMARY KEY (IDREP)
) ;

CREATE TABLE PROVEEDORES (
    IDPRO integer  NOT NULL,
    NOMPRO varchar2(50)  NOT NULL,
    RUCPRO integer  NOT NULL,
    DIRPRO varchar2(50)  NOT NULL,
    PROPRO varchar2(50)  NOT NULL,
    CELPRO char(9)  NOT NULL,
    ESTPRO char(1)  NOT NULL,
    CONSTRAINT PROVEEDOR_pk PRIMARY KEY (IDPRO)
) ;

CREATE TABLE COMPRA (
    IDCOM integer  NOT NULL,
    FECCOM date  NOT NULL,
    FOR_PAG varchar2(50) NOT NULL,
    IDEMP integer  NOT NULL,
    IDPRO integer  NOT NULL,
    CONSTRAINT COMPRA_pk PRIMARY KEY (IDCOM)
) ;

CREATE TABLE COMPRA_DETALLE (
    IDCOMDET integer  NOT NULL,
    CANTCOMDET integer  NOT NULL,
    IDREP int  NOT NULL,
    IDMOT int NOT NULL,
    IDCOM int NOT NULL,
    CONSTRAINT COMPRA_DETALLE_pk PRIMARY KEY (IDCOMDET)
) ;

CREATE TABLE VENTA (
    IDVEN integer  NOT NULL,
    FECVEN date  NOT NULL,
    FOR_PAG varchar2(50) NOT NULL,
    IDCLI integer  NOT NULL,
    IDEMP integer  NOT NULL,
    CONSTRAINT VENTA_pk PRIMARY KEY (IDVEN)
) ;

CREATE TABLE VENTA_DETALLE (
    IDVENDET integer  NOT NULL,
