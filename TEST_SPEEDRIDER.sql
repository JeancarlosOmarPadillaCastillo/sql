CREATE VIEW vVenta
AS
SELECT
V.FOR_PAG,
mo.catmot || ' '||mo.modmot || ' '||mo.marmot AS moto,
mo.prevenmot,
mo.prevenmot * VD.CANTVENDET as importe,
C.nomclie || ' ' || C.apeclie AS NOMBRES,
c.docclie,
VD.CANTVENDET,
VD.IDVENT
FROM
VENTA  V JOIN VENTA_DETALLE  VD
ON V.IDVEN=VD.IDVENT JOIN cliente C ON V.IDCLI= C.IDCLI JOIN motos MO on vd.idmot = mo.idmot
;

CREATE OR REPLACE VIEW ProductoTop AS
SELECT mot.marmot, SUM(VDT.CANTVENDET) AS Cantidad FROM VENTA_DETALLE VDT
INNER JOIN motos mot
ON VDT.IDMOT = mot.IDMOT
GROUP BY mot.marmot
ORDER BY Cantidad DESC ;
/

